    /**
     * 计算真太阳时
     * 根据出生地的经度调整时间
     * 东八区标准经度为120度，每度经度差对应4分钟时间差
     * 
     * @param string $birthDate 出生日期时间 (Y-m-d H:i:s)
     * @param float $longitude 经度 (-180 to 180)
     * @return array 调整后的时间信息
     */
    protected function calculateTrueSolarTime(string $birthDate, float $longitude): array
    {
        // 东八区标准经度
        $standardLongitude = 120.0;
        
        // 计算经度差
        $longitudeDiff = $longitude - $standardLongitude;
        
        // 每度经度差对应4分钟（1度 = 4分钟 = 240秒）
        $timeDiffSeconds = $longitudeDiff * 240;
        
        // 创建日期时间对象
        $dateTime = new \DateTime($birthDate);
        
        // 如果有经度差，调整时间
        if ($timeDiffSeconds != 0) {
            $interval = new \DateInterval('PT' . abs((int)$timeDiffSeconds) . 'S');
            if ($timeDiffSeconds > 0) {
                // 东经大于120度，时间比北京时间早，需要减去差值
                $dateTime->sub($interval);
            } else {
                // 东经小于120度，时间比北京时间晚，需要加上差值
                $dateTime->add($interval);
            }
            
            return [
                'adjusted' => true,
                'original_time' => $birthDate,
                'adjusted_time' => $dateTime->format('Y-m-d H:i:s'),
                'longitude' => $longitude,
                'longitude_diff' => $longitudeDiff,
                'time_diff_minutes' => round($timeDiffSeconds / 60, 2),
                'adjustment_desc' => $this->getTrueSolarTimeDesc($longitude, $longitudeDiff, $timeDiffSeconds),
            ];
        }
        
        // 经度为120度或没有经度数据，无需调整
        return [
            'adjusted' => false,
            'original_time' => $birthDate,
            'adjusted_time' => $birthDate,
            'longitude' => $longitude,
        ];
    }
    
    /**
     * 获取真太阳时调整描述
     */
    protected function getTrueSolarTimeDesc(float $longitude, float $longitudeDiff, float $timeDiffSeconds): string
    {
        $direction = $longitudeDiff > 0 ? '东' : '西';
        $absDiff = abs($longitudeDiff);
        $absMinutes = abs($timeDiffSeconds / 60);
        $minutes = floor($absMinutes);
        $seconds = round(($absMinutes - $minutes) * 60);
        
        $timeDesc = $minutes > 0 ? "{$minutes}分" : "";
        $timeDesc .= $seconds > 0 ? "{$seconds}秒" : "";
        
        if ($longitudeDiff > 0) {
            return "出生地位于东经{$longitude}度，比北京偏{$direction}{$absDiff}度，当地时间比北京时间早{$timeDesc}";
        } else {
            return "出生地位于东经{$longitude}度，比北京偏{$direction}{$absDiff}度，当地时间比北京时间晚{$timeDesc}";
        }
    }

     * 立春前属于上一年
     */
    protected function getLunarYear(int $year, int $month, int $day): int
    {
        // 获取该年立春日期
        $lichun = $this->getJieQiDate($year, '立春');
        
        // 如果还没到立春，属于上一年
        if ($month < $lichun[0] || ($month == $lichun[0] && $day < $lichun[1])) {
            return $year - 1;
        }
        return $year;
    }
    

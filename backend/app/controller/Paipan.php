    /**
     * 八字排盘
     */
    public function bazi()
    {
        $data = $this->request->post();
        $user = $this->request->user;
        
        // 验证参数
        if (empty($data['birthDate'])) {
            return $this->error('请提供出生日期');
        }
        
        if (empty($data['gender'])) {
            return $this->error('请提供性别');
        }
        
        // 解析出生日期
        $birthDate = $data['birthDate'];
        $gender = $data['gender'];
        $location = $data['location'] ?? '';
        $longitude = $data['longitude'] ?? null; // 经度，用于真太阳时计算
        $mode = $data['mode'] ?? 'pro'; // 'simple' or 'pro'
        
        // 如果有经度数据，计算真太阳时
        $trueSolarTimeInfo = null;
        if ($longitude !== null && is_numeric($longitude)) {
            $trueSolarTimeResult = $this->calculateTrueSolarTime($birthDate, (float)$longitude);
            if ($trueSolarTimeResult['adjusted']) {
                $trueSolarTimeInfo = $trueSolarTimeResult;
                $birthDate = $trueSolarTimeResult['adjusted_time'];
            }
        }
        
        // 计算八字（在事务外计算，减少锁持有时间）
        $bazi = $this->calculateBazi($birthDate);
        
        // 使用专业解读服务生成分析
        $fullInterpretation = $this->interpretationService->generateFullInterpretation($bazi, $gender);
        $simpleInterpretation = $this->interpretationService->generateSimpleInterpretation($bazi, $gender);
        
        // 生成详细分析报告
        $analysis = $this->generateDetailedAnalysis($fullInterpretation);
        
        // 计算大运（专业版）
        $daYun = [];
        $liuNian = [];
        $fortuneAnalysis = null;
        if ($mode === 'pro') {
            $daYun = $this->calculateDaYun($bazi, $gender, $birthDate);
            $daYun = $this->analyzeDaYunTrend($daYun, $bazi);
            
            // 计算流年（最近5年）
            $currentYear = (int)date('Y');
            $liuNian = $this->calculateLiuNian($currentYear, 5);
            
            // 大运流年综合分析
            $fortuneAnalysis = $this->fortuneAnalysisService->analyzeDaYunLiuNian($bazi, $daYun, $liuNian);
        }
        
        // ===== 使用数据库行锁防止并发问题 =====
        Db::startTrans();
        try {
            // 1. 使用行锁查询用户，防止并发修改积分
            $userData = Db::name('tc_user')
                ->where('id', $user['sub'])
                ->where('status', 1)
                ->lock(true)
                ->find();
            
            if (!$userData) {
                Db::rollback();
                return $this->error('用户不存在', 404);
            }
            
            // 2. 检查是否是首次排盘（基于积分记录，防止删除记录后重复获取免费）
            $hasBaziRecord = BaziRecord::where('user_id', $user['sub'])->count() > 0;
            $hasPointsRecord = PointsRecord::where('user_id', $user['sub'])
                ->where('type', 'bazi')
                ->count() > 0;
            $isFirstBazi = (!$hasBaziRecord && !$hasPointsRecord);
            
            // 3. 非首次排盘需要检查积分
            if (!$isFirstBazi && $userData['points'] < self::BAZI_POINTS_COST) {
                Db::rollback();
                return $this->error('积分不足，请先充值', 403);
            }
            
            $pointsCost = $isFirstBazi ? 0 : self::BAZI_POINTS_COST;
            
            // 4. 非首次排盘扣除积分（使用原子操作）
            if (!$isFirstBazi) {
                $updateResult = Db::name('tc_user')
                    ->where('id', $user['sub'])
                    ->dec('points', self::BAZI_POINTS_COST)
                    ->update();
                
                if ($updateResult === 0) {
                    Db::rollback();
                    return $this->error('积分扣除失败，请重试');
                }
                
                // 记录积分变动
                Db::name('tc_points_record')->insert([
                    'user_id' => $user['sub'],
                    'action' => '八字排盘消耗',
                    'points' => -self::BAZI_POINTS_COST,
                    'type' => 'bazi',
                    'related_id' => 0,
                    'remark' => "排盘日期: {$birthDate}",
                    'created_at' => date('Y-m-d H:i:s'),
                ]);
            }
            
            // 保存排盘记录
            $record = BaziRecord::create([
                'user_id' => $user['sub'],
                'birth_date' => $birthDate,
                'gender' => $gender,
                'location' => $location,
                'year_gan' => $bazi['year']['gan'],
                'year_zhi' => $bazi['year']['zhi'],
                'month_gan' => $bazi['month']['gan'],
                'month_zhi' => $bazi['month']['zhi'],
                'day_gan' => $bazi['day']['gan'],
                'day_zhi' => $bazi['day']['zhi'],
                'hour_gan' => $bazi['hour']['gan'],
                'hour_zhi' => $bazi['hour']['zhi'],
                'analysis' => $analysis,
                'is_first' => $isFirstBazi,
            ]);
            
            Db::commit();
            
            // 查询最新的积分余额
            $remainingPoints = Db::name('tc_user')
                ->where('id', $user['sub'])
                ->value('points');
            
            $result = [
                'id' => $record->id,
                'bazi' => $bazi,
                'analysis' => $analysis,
                'simpleInterpretation' => $simpleInterpretation,
                'fullInterpretation' => $fullInterpretation,
                'points_cost' => $pointsCost,
                'remaining_points' => $remainingPoints,
                'is_first_bazi' => $isFirstBazi,
                'true_solar_time' => $trueSolarTimeInfo,
            ];
            
            // 专业版额外数据
            if ($mode === 'pro') {
                $result['dayun'] = $daYun;
                $result['liunian'] = $liuNian;
                $result['fortune_analysis'] = $fortuneAnalysis;
            }
            
            // 缓存排盘结果（用于复用）
            if (self::ENABLE_CACHE) {
                $cacheKey = CacheService::baziKey($birthDate, $gender);
                $cacheData = [
                    'bazi' => $bazi,
                    'analysis' => $analysis,
                    'simpleInterpretation' => $simpleInterpretation,
                    'fullInterpretation' => $fullInterpretation,
                    'dayun' => $daYun ?? [],
                    'liunian' => $liuNian ?? [],
                    'fortune_analysis' => $fortuneAnalysis ?? null,
                ];
                CacheService::set($cacheKey, $cacheData, CacheService::TTL_WEEK, CacheService::TAG_BAZI);
            }
            
            return $this->success($result);
        } catch (\Exception $e) {
            Db::rollback();
            Log::error('八字排盘失败: ' . $e->getMessage(), [
                'user_id' => $user['sub'] ?? 0,
                'birth_date' => $birthDate ?? '',
                'trace' => $e->getTraceAsString()
            ]);
            return $this->error('排盘失败，请稍后重试', 500);
        }
    }
    
    /**
     * 处理缓存结果
     * 复用缓存的八字计算结果，但仍需记录和扣除积分
     */
    protected function processCachedResult(array $cachedResult, array $user, string $birthDate, string $mode)
    {
        $userModel = \app\model\User::find($user['sub']);
        if (!$userModel) {
            return $this->error('用户不存在', 404);
        }
        
        // 检查是否是首次排盘（基于积分记录，防止删除记录后重复获取免费）
        $hasBaziRecord = BaziRecord::where('user_id', $user['sub'])->count() > 0;
        $hasPointsRecord = PointsRecord::where('user_id', $user['sub'])
            ->where('type', 'bazi')
            ->count() > 0;
        $isFirstBazi = (!$hasBaziRecord && !$hasPointsRecord);
        
        // 非首次排盘需要检查积分
        if (!$isFirstBazi && $userModel->points < self::BAZI_POINTS_COST) {
            return $this->error('积分不足，请先充值', 403);
        }
        
        Db::startTrans();
        try {
            $pointsCost = $isFirstBazi ? 0 : self::BAZI_POINTS_COST;
            
            // 非首次排盘扣除积分
            if (!$isFirstBazi) {
                $userModel->deductPoints(self::BAZI_POINTS_COST);
                
                // 记录积分变动
                PointsRecord::record(
                    $user['sub'],
                    '八字排盘消耗',
                    -self::BAZI_POINTS_COST,
                    'bazi',
                    0,
                    "排盘日期: {$birthDate} (使用缓存结果)"
                );
            }
            
            // 保存排盘记录
            $record = BaziRecord::create([
                'user_id' => $user['sub'],
                'birth_date' => $birthDate,
                'gender' => 'unknown', // 从缓存获取时可能不知道性别
                'location' => '',
                'year_gan' => $cachedResult['bazi']['year']['gan'],
                'year_zhi' => $cachedResult['bazi']['year']['zhi'],
                'month_gan' => $cachedResult['bazi']['month']['gan'],
                'month_zhi' => $cachedResult['bazi']['month']['zhi'],
                'day_gan' => $cachedResult['bazi']['day']['gan'],
                'day_zhi' => $cachedResult['bazi']['day']['zhi'],
                'hour_gan' => $cachedResult['bazi']['hour']['gan'],
                'hour_zhi' => $cachedResult['bazi']['hour']['zhi'],
                'analysis' => $cachedResult['analysis'],
                'is_first' => $isFirstBazi,
            ]);
            
            Db::commit();
            
            $result = [
                'id' => $record->id,
                'bazi' => $cachedResult['bazi'],
                'analysis' => $cachedResult['analysis'],
                'simpleInterpretation' => $cachedResult['simpleInterpretation'],
                'points_cost' => $pointsCost,
                'remaining_points' => $userModel->points,
                'is_first_bazi' => $isFirstBazi,
                'from_cache' => true,
            ];
            
            // 专业版额外数据
            if ($mode === 'pro') {
                $result['dayun'] = $cachedResult['dayun'] ?? [];
                $result['liunian'] = $cachedResult['liunian'] ?? [];
                $result['fortune_analysis'] = $cachedResult['fortune_analysis'] ?? null;
            }
            
            return $this->success($result);
        } catch (\Exception $e) {
            Db::rollback();
            // 记录详细错误到日志，但返回通用错误信息给用户（安全修复）
            Log::error('排盘失败: ' . $e->getMessage(), [
                'file' => $e->getFile(),
                'line' => $e->getLine(),
                'trace' => $e->getTraceAsString()
            ]);
            return $this->error('排盘失败，请稍后重试', 500);
        }
    }
    
    /**
     * 获取排盘历史（支持后端分页）
